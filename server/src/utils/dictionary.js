function buildZhToEnDict(enToZh) {
  const zhToEn = {};
  for (const [enWord, zhWord] of Object.entries(enToZh)) {
    if (!zhToEn[zhWord]) {
      zhToEn[zhWord] = enWord;
    }
  }
  return zhToEn;
}

const zhToEnDict = buildZhToEnDict(enToZhDict);

function hasChinese(text) {
  return /[\u4e00-\u9fa5]/.test(text);
}

function hasEnglish(text) {
  return /[a-zA-Z]/.test(text);
}

function translateEnToZh(text) {
  if (!text || !text.trim()) return text;
  
  let result = text;
  
  const sortedPhrases = Object.keys(enToZhDict).sort((a, b) => b.length - a.length);
  
  for (const phrase of sortedPhrases) {
    if (phrase.includes(' ')) {
      const regex = new RegExp('\\b' + phrase.replace(/[.*+?^${}()|[\]\\]/g, '\\$&') + '\\b', 'gi');
      result = result.replace(regex, enToZhDict[phrase]);
    }
  }
  
  const words = result.split(/(\s+|[.,!?;:'"()\[\]{}])/);
  
  for (let i = 0; i < words.length; i++) {
    const word = words[i];
    const lowerWord = word.toLowerCase();
    
    if (/^[a-zA-Z]+$/.test(word) && enToZhDict[lowerWord]) {
      const translation = enToZhDict[lowerWord];
      if (word[0] === word[0].toUpperCase() && word.length > 1) {
        words[i] = translation;
      } else {
        words[i] = translation;
      }
    }
  }
  
  return words.join('');
}

function translateZhToEn(text) {
  if (!text || !text.trim()) return text;
  
  let result = text;
  
  const sortedPhrases = Object.keys(zhToEnDict).sort((a, b) => b.length - a.length);
  
  for (const phrase of sortedPhrases) {
    if (phrase.length > 1) {
      while (result.includes(phrase)) {
        result = result.replace(phrase, ' ' + zhToEnDict[phrase] + ' ');
      }
    }
  }
  
  for (const char of result) {
    if (/[\u4e00-\u9fa5]/.test(char) && zhToEnDict[char]) {
      result = result.replace(char, ' ' + zhToEnDict[char] + ' ');
    }
  }
  
  result = result.replace(/\s+/g, ' ').trim();
  
  return result;
}

function translateText(text, sourceLang, targetLang) {
  if (!text || !text.trim()) return text;
  
  const normalizedSource = (sourceLang || 'auto').toLowerCase();
  const normalizedTarget = (targetLang || 'zh').toLowerCase();
  
  if (normalizedSource === normalizedTarget) {
    return text;
  }
  
  if (normalizedSource === 'auto') {
    if (hasChinese(text)) {
      if (normalizedTarget === 'en') {
        return translateZhToEn(text);
      }
      return text;
    } else {
      if (normalizedTarget === 'zh') {
        return translateEnToZh(text);
      }
      return text;
    }
  }
  
  if (normalizedSource === 'en' && normalizedTarget === 'zh') {
    return translateEnToZh(text);
  }
  
  if (normalizedSource === 'zh' && normalizedTarget === 'en') {
    return translateZhToEn(text);
  }
  
  return text;
}

